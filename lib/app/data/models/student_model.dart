
import 'dart:ui';

class StudentModel {
  final String name;
  final String initials;
  final String gender;
  final String status;
  final Color avatarColor;
  final Color statusColor;

  StudentModel({
    required this.name,
    required this.initials,
    required this.gender,
    required this.status,
    required this.avatarColor,
    required this.statusColor,
  });
}